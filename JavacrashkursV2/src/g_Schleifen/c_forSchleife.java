package g_Schleifen;

public class c_forSchleife {
    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) { //Anfangswert = 0; solange i kleiner oder gleich 10 ist (i < 10) addiere 1 in jeder Schleife(i++)
            System.out.println(i);     //und gib den resultierenden Wert in der Konsole aus 
        }
        
    }

}

/* Schleifen ermöglichen das Widerholte ausführen von Codeblöcken, bis das gewünschte ergebnis erreicht wird
 * Mit schleifen ist es möglich Datenstrukturen wie z.B. Arrays und Listen zu befüllen
 * 
 * ----------------------------------------------------------
 * 
 * Eine for-Schleife wird auch Zählerschleife genannt und wird genutzt wenn wir genau wissen, wie oft eine Schleife ausgeführt werden soll
 * Die for-Schleife ist KOPFGESTEUERT
 */